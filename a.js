// Script para converter lista de preços de frontais para JSON estruturado

function converterListaParaJSON(textoOriginal) {
    const linhas = textoOriginal.split('\n').filter(linha => linha.trim() !== '');
    const produtos = [];
    let id = 1;
    let marcaAtual = '';
    
    // Regex para identificar preços no formato R$ XXX,XX
    const regexPreco = /R\$\s*(\d+(?:,\d{2})?)/g;
    
    linhas.forEach(linha => {
        linha = linha.trim();
        
        // Identificar mudança de marca
        if (linha === 'MOTOROLA' || linha === 'SAMSUNG' || linha === 'IPHONE' || 
            linha === 'XIAOMI-REDMI' || linha === 'LG' || linha === 'ZENFONE ASSUS' || 
            linha === 'REALME') {
            marcaAtual = linha.toLowerCase().replace('-redmi', '').replace(' assus', '');
            return;
        }
        
        // Pular linhas vazias ou que não contêm produtos
        if (!linha.startsWith('FRONTAL') || marcaAtual === '') {
            return;
        }
        
        // Extrair todos os preços da linha
        const precos = [...linha.matchAll(regexPreco)];
        
        if (precos.length === 0) {
            // Produto sem preço definido
            const nomeLimpo = linha.replace(/R\$.*$/, '').trim();
            produtos.push({
                id: id++,
                nome: nomeLimpo,
                preco: "A consultar",
                marca: marcaAtual,
                categoria: "frontal",
                disponivel: false,
                dataAtualizacao: "2025-08-17"
            });
        } else {
            // Para cada preço encontrado, criar variações do produto
            precos.forEach((match, index) => {
                const precoCompleto = match[0];
                let nomeProduto = linha.substring(0, match.index).trim();
                
                // Se houver texto após o preço, pode ser uma variação
                const textoAposPreco = linha.substring(match.index + match[0].length).trim();
                
                // Identificar variações específicas
                let sufixoVariacao = '';
                if (nomeProduto.includes('SEM ARO')) {
                    sufixoVariacao = ' (SEM ARO)';
                    nomeProduto = nomeProduto.replace('SEM ARO', '').trim();
                } else if (nomeProduto.includes('COM ARO')) {
                    sufixoVariacao = ' (COM ARO)';
                    nomeProduto = nomeProduto.replace('COM ARO', '').trim();
                } else if (nomeProduto.includes('VIVAD')) {
                    sufixoVariacao = ' (VIVAD)';
                    nomeProduto = nomeProduto.replace('VIVAD', '').trim();
                } else if (nomeProduto.includes('ORI')) {
                    sufixoVariacao = ' (ORIGINAL)';
                    nomeProduto = nomeProduto.replace('ORI', '').trim();
                } else if (nomeProduto.includes('INCEL')) {
                    sufixoVariacao = ' (INCELL)';
                    nomeProduto = nomeProduto.replace('INCEL', '').trim();
                } else if (nomeProduto.includes('NACIONAL')) {
                    sufixoVariacao = ' (NACIONAL)';
                    nomeProduto = nomeProduto.replace('NACIONAL', '').trim();
                }
                
                // Se houver texto após o preço que indica variação
                if (textoAposPreco && index < precos.length - 1) {
                    const proximoPreco = precos[index + 1];
                    const textoVariacao = linha.substring(match.index + match[0].length, proximoPreco.index).trim();
                    if (textoVariacao.includes('COM ARO')) {
                        sufixoVariacao = ' (COM ARO)';
                    } else if (textoVariacao.includes('VIVAD')) {
                        sufixoVariacao = ' (VIVAD)';
                    }
                }
                
                produtos.push({
                    id: id++,
                    nome: (nomeProduto + sufixoVariacao).trim(),
                    preco: precoCompleto,
                    marca: marcaAtual,
                    categoria: "frontal",
                    disponivel: true,
                    dataAtualizacao: "2025-08-17"
                });
            });
        }
    });
    
    return {
        produtos: produtos,
        totalProdutos: produtos.length,
        marcas: [...new Set(produtos.map(p => p.marca))],
        ultimaAtualizacao: "2025-08-17"
    };
}

